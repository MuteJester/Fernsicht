const powershellCompletion = `# fernsicht completion for PowerShell. Install:
#   fernsicht completion powershell >> $PROFILE
# Reload your profile (or open a new shell) afterwards.

Register-ArgumentCompleter -Native -CommandName fernsicht -ScriptBlock {
    param($wordToComplete, $commandAst, $cursorPosition)

    $commands = @(
        @{name='run';        desc='Wrap a command; broadcast progress'}
        @{name='url';        desc='Print URL of running session(s)'}
        @{name='doctor';     desc='Diagnose installation / network'}
        @{name='magic';      desc='Magic-prefix protocol reference'}
        @{name='completion'; desc='Generate shell completion'}
        @{name='update';     desc='Check for / install a newer version'}
        @{name='version';    desc='Print version + build info'}
        @{name='help';       desc='Show help'}
    )

    $tokens = $commandAst.CommandElements
    if ($tokens.Count -le 1) {
        $commands | Where-Object { $_.name -like "$wordToComplete*" } | ForEach-Object {
            [System.Management.Automation.CompletionResult]::new($_.name, $_.name,
                'ParameterValue', $_.desc)
        }
        return
    }

    $cmd = $tokens[1].Value
    switch ($cmd) {
        'completion' {
            'bash','zsh','fish','powershell' | Where-Object { $_ -like "$wordToComplete*" } | ForEach-Object {
                [System.Management.Automation.CompletionResult]::new($_, $_, 'ParameterValue', "$_ shell")
            }
        }
        'url' {
            '--all','--pid' | Where-Object { $_ -like "$wordToComplete*" } | ForEach-Object {
                [System.Management.Automation.CompletionResult]::new($_, $_, 'ParameterName', "url flag")
            }
        }
        'doctor' {
            '--explain','--server-url','--no-color' | Where-Object { $_ -like "$wordToComplete*" } | ForEach-Object {
                [System.Management.Automation.CompletionResult]::new($_, $_, 'ParameterName', "doctor flag")
            }
        }
        'update' {
            '--check' | Where-Object { $_ -like "$wordToComplete*" } | ForEach-Object {
                [System.Management.Automation.CompletionResult]::new($_, $_, 'ParameterName', "update flag")
            }
        }
        'run' {
            $runFlags = @(
                '--no-pty','--no-unbuffer','--debug',
                '--no-detect','--no-magic','--strict-magic',
                '--server-url','--join-secret','--max-viewers','--label',
                '--share','--quiet','--qr','--no-qr','--no-fail-on-bridge',
                '--unit','--pattern','--copy-url','--strict','--output',
                '--config','--url-file','--webhook'
            )
            $runFlags | Where-Object { $_ -like "$wordToComplete*" } | ForEach-Object {
                [System.Management.Automation.CompletionResult]::new($_, $_, 'ParameterName', "run flag")
            }
        }
    }
}
`
